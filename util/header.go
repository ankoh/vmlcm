package util

import (
	"fmt"
)

// PrintASCIIHeader prints an ASCII header
func PrintASCIIHeader() {
	fmt.Println("                    __             ")
	fmt.Println("   _   ______ ___  / /________ ___ ")
	fmt.Println("  | | / / __ `__ \\/ / ___/ __ `__ \\")
	fmt.Println("  | |/ / / / / / / / /__/ / / / / /")
	fmt.Println("  |___/_/ /_/ /_/_/\\___/_/ /_/ /_/ ")
	fmt.Println("                                   ")
}
